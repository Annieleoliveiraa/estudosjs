
/* 
Faça um programa que leia três notas, 
calcule e mostre a média aritmética entre elas 
*/

// Criando as variáveis e entrada de dados do usuário 
let nota1 = Number(prompt("Digite um número"))
let nota2 = Number(prompt("Digite um segundo número"))
let nota3 = Number(prompt("Digite um terceiro número"))

// Imprimindo o valor na tela 
console.log("A média aritmética é: " + (nota1 + nota2 + nota3) / 3)
